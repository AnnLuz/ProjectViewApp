    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed
       
       String EMAIL_REGEX = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";
       Pattern EMAIL_PATTERN = Pattern.compile(EMAIL_REGEX);
       String PHONE_REGEX = "^(\\+?\\d[\\d\\s-]{4,13}\\d)$";
       nombre =  txtPantallaNombre.getText();
       email = txtPantallaEmail.getText();
       telefono = txtPantallaTelefono.getText();
       
       if(!(nombre.isEmpty()) && !(email.isEmpty()) && !(telefono.isEmpty())){
       
            if(!(nombre == null) || !(email == null) || !(telefono == null)){

                if(EMAIL_PATTERN.matcher(email).matches()){
                    
                    if(telefono.matches(PHONE_REGEX)){
                        Contacto.addContacto(new Contacto(nombre,email,telefono));
                        System.out.println(Contacto.getContactos());
                        JOptionPane.showMessageDialog(
                        this,
                        "Proceso exitoso" ,
                        "Los datos ingresados fueron registrados.\n",
                        JOptionPane.INFORMATION_MESSAGE
                        );
                    
                        nombre = "";
                        email = "";
                        telefono = "";
                        txtPantallaNombre.setText("");
                        txtPantallaEmail.setText("");
                        txtPantallaTelefono.setText("");
                    }else{
                    
                        JOptionPane.showMessageDialog(
                         
                        this,
                        "Numero de telefono no v√°lido (6-14 digitos)" ,
                        "Ingrese nuevamente el n√∫mero.\n",
                        JOptionPane.WARNING_MESSAGE
                                
                        );
                        telefono = "";
                        txtPantallaTelefono.setText("");
                    }

                }else{
                
                    JOptionPane.showMessageDialog(
                         
                        this,
                        "Email no v√°lido" ,
                        "Ingrese nuevamente el email.\n",
                        JOptionPane.WARNING_MESSAGE
                    );
                    email = "";
                    txtPantallaEmail.setText("");
                
                }
               
            }else{
       
                    JOptionPane.showMessageDialog(
                         
                        this,
                        "Proceso denegado" ,
                        "Ingrese los datos de cada compo.\n",
                        JOptionPane.WARNING_MESSAGE
                    );
            }
        }else{
       
       
            JOptionPane.showMessageDialog(
                         
                this,
                "Proceso denegado" ,
                "Ingrese los datos de cada compo.\n",
                JOptionPane.WARNING_MESSAGE
            );
            nombre = "";
            email = "";
            telefono = "";
            txtPantallaNombre.setText("");
            txtPantallaEmail.setText("");
            txtPantallaTelefono.setText("");
       }
 
    }//GEN-LAST:event_botonAceptarActionPerformed

